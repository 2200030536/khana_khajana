import mongoose from 'mongoose';

const studentUserSchema = new mongoose.Schema({
  id: {
    type: Number,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  },
  department: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  }
});

const StudentUser = mongoose.model('StudentUser', studentUserSchema);

export default StudentUser;